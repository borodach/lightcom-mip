CREATE TABLE CONNECTIONS 
(   OBJECTID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    FKLOGINS INT NOT NULL, 
    LAST_ACCESS DATETIME NOT NULL,
    EKEY TEXT,
    DKEY TEXT,
    IP VARCHAR (32),
    HOST VARCHAR (255),
    INDEX (FKLOGINS),
    INDEX (LAST_ACCESS),
    INDEX (FKLOGINS, LAST_ACCESS)
)
